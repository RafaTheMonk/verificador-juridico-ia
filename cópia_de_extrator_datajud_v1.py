def extrair_processos(url, tribunal, ano):
    """
    Extrai processos de um tribunal/ano específico usando paginação search_after.
    """
    data_inicio = f"{ano}-01-01"
    data_fim = f"{ano}-12-31"

    processos = []
    pagina = 0
    search_after = None

    while True:
        pagina += 1

        # Monta a query seguindo o padrão da documentação
        payload = {
            "size": TAMANHO_PAGINA,
            "query": {
                "bool": {
                    "must": [
                        {"match": {"assuntos.codigo": CODIGO_ASSUNTO}}
                    ],
                    "filter": [
                        {"range": {"dataAjuizamento": {"gte": data_inicio, "lte": data_fim}}},
                        {"term": {"nivelSigilo": 0}}
                    ]
                }
            },
            "sort": [{"@timestamp": {"order": "asc"}}]
        }

        if search_after:
            payload["search_after"] = search_after

        time.sleep(DELAY_REQUISICAO)
        resposta = fazer_requisicao(url, payload)

        if not resposta:
            break

        hits = resposta.get("hits", {}).get("hits", [])
        total_info = resposta.get("hits", {}).get("total", {})

        # Primeira página: mostra total
        if pagina == 1:
            total_val = total_info.get("value", 0)
            rel = total_info.get("relation", "eq")
            sufixo = "+" if rel == "gte" else ""
            print(f"      {tribunal} {ano}: {total_val}{sufixo} processos encontrados")

            if total_val == 0:
                break

        if not hits:
            break

        # Adiciona os processos
        for hit in hits:
            proc = hit.get("_source", {})
            proc["_tribunal_consulta"] = tribunal  # Marca o tribunal
            proc["_ano_consulta"] = ano            # Marca o ano
            processos.append(proc)

        # Prepara próxima página
        search_after = hits[-1].get("sort")
        if not search_after or len(hits) < TAMANHO_PAGINA:
            break

    return processos


def processar_dataframe(processos):
    """Converte lista de processos para DataFrame tabular."""
    if not processos:
        return pd.DataFrame()

    print(f"\n🔄 Processando {len(processos)} processos...")

    dados = []
    for proc in processos:
        linha = {
            # Metadados da consulta
            "tribunal_fonte": proc.get("_tribunal_consulta", ""),
            "ano_consulta": proc.get("_ano_consulta", ""),

            # Identificação
            "id": proc.get("id", ""),
            "numeroProcesso": proc.get("numeroProcesso", ""),
            "tribunal": proc.get("tribunal", ""),
            "grau": proc.get("grau", ""),

            # Datas
            "dataAjuizamento": proc.get("dataAjuizamento", ""),

            # Classe
            "classe_codigo": (proc.get("classe") or {}).get("codigo", ""),
            "classe_nome": (proc.get("classe") or {}).get("nome", ""),

            # Assuntos (concatenados)
            "assuntos_codigos": " | ".join(str(a.get("codigo", "")) for a in (proc.get("assuntos") or [])),
            "assuntos_nomes": " | ".join(a.get("nome", "") for a in (proc.get("assuntos") or [])),

            # Órgão julgador
            "orgao_codigo": (proc.get("orgaoJulgador") or {}).get("codigo", ""),
            "orgao_nome": (proc.get("orgaoJulgador") or {}).get("nome", ""),
            "municipio_ibge": (proc.get("orgaoJulgador") or {}).get("codigoMunicipioIBGE", ""),

            # Sistema e formato
            "sistema": (proc.get("sistema") or {}).get("nome", ""),
            "formato": (proc.get("formato") or {}).get("nome", ""),

            # Sigilo
            "nivelSigilo": proc.get("nivelSigilo", ""),

            # Movimentos (resumo)
            "qtd_movimentos": len(proc.get("movimentos") or []),

            # Timestamps
            "ultimaAtualizacao": proc.get("dataHoraUltimaAtualizacao", ""),
            "timestamp": proc.get("@timestamp", ""),
        }

        # Último movimento
        movimentos = proc.get("movimentos") or []
        if movimentos:
            movs_ordenados = sorted(
                [m for m in movimentos if m.get("dataHora")],
                key=lambda x: x.get("dataHora", ""),
                reverse=True
            )
            if movs_ordenados:
                linha["ultimo_mov_data"] = movs_ordenados[0].get("dataHora", "")
                linha["ultimo_mov_nome"] = movs_ordenados[0].get("nome", "")

        dados.append(linha)

    df = pd.DataFrame(dados)

    # Extrai ano do ajuizamento para facilitar análises
    if "dataAjuizamento" in df.columns:
        df["ano_ajuizamento"] = pd.to_datetime(df["dataAjuizamento"], errors="coerce").dt.year

    print(f"✅ DataFrame: {len(df)} linhas × {len(df.columns)} colunas")
    return df


def exportar_excel(df, arquivo):
    """Exporta DataFrame para Excel com formatação."""
    if df.empty:
        print("⚠️ Nenhum dado para exportar.")
        return False

    print(f"\n💾 Salvando: {arquivo}")

    with pd.ExcelWriter(arquivo, engine="openpyxl") as writer:
        # Aba principal com todos os dados
        df.to_excel(writer, sheet_name="Processos", index=False)

        # Aba de resumo
        resumo = df.groupby(["tribunal_fonte", "ano_consulta"]).size().unstack(fill_value=0)
        resumo.to_excel(writer, sheet_name="Resumo")

        # Formatação
        ws = writer.sheets["Processos"]
        ws.freeze_panes = "A2"

    print(f"✅ Arquivo salvo com sucesso!")
    return True


