def fazer_requisicao(url, payload, tentativa=1, max_tentativas=5):
    """Faz requisição à API com retry e backoff exponencial."""
    headers = {
        'Authorization': f'ApiKey {API_KEY}',
        'Content-Type': 'application/json'
    }

    try:
        response = requests.post(url, headers=headers, data=json.dumps(payload), timeout=60)

        if response.status_code == 200:
            return response.json()
        elif response.status_code == 429 or response.status_code >= 500:
            if tentativa < max_tentativas:
                tempo_espera = (2 ** tentativa)
                print(f"      ⚠️ Erro {response.status_code}. Retry em {tempo_espera}s...")
                time.sleep(tempo_espera)
                return fazer_requisicao(url, payload, tentativa + 1, max_tentativas)
        else:
            print(f"      ❌ Erro {response.status_code}: {response.text[:200]}")
            return None
    except Exception as e:
        if tentativa < max_tentativas:
            time.sleep(2 ** tentativa)
            return fazer_requisicao(url, payload, tentativa + 1, max_tentativas)
        print(f"      ❌ Erro de conexão: {e}")
        return None


